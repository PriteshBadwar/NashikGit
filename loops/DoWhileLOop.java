package loops;

public class DoWhileLOop {
public static void main(String[] args) {
	// In the do-while loop first do loop will execuate with respect to while loop 
	//then the while loop will execuate then base on the boolean condition ..
	
	int i=2;
	do {
		System.out.println("Hello");
		i=i+1;
		}
	while(i>10);
	
	
	
	
	
	
}
}
