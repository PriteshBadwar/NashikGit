package loops;

public class UsingForLoopPrint2x2Table {
	
public static void main(String[] args) {
	
	int no=2;
	for(int i=0;i<10;i=i+1) 
	{
		int multiresult = i*1;
	System.out.println(no+"*"+i+"="+multiresult);	
	}
	
	//Print Five Star
	
	for(int i=0;i<=5;i=i+1)
	{
		System.out.println("*");
	}
	
	
	
	
}
}
